#include <stdio.h>

/**
 * main - Checks if a random number is positive or negative or zero
 * Return: returns zero
 */
void positive_or_negative(int n)
{
	if (n > 0)
	{
		printf("%d is positive\n", n);
	}
	else if (n == 0)
	{
		printf("%d is zero\n", n);
	}
	else
	{
		printf("%d is negative\n", n);
	}
}

